#!/usr/bin/env python3
"""Export the paper's real-world throughput table from collected results.

Recomputes everything from the per-run *_raw.csv files (the single source of
truth) with the project statistic — trim-2 mean ± sample std (drop the two
lowest and two highest runs when n > 4, else one of each when n > 2).

Usage:
  tools/export_paper_table.py <results-dir> [more-dirs...] [--format md|latex|csv]

<results-dir> is scanned recursively for *_raw.csv, so all of these work:
  tools/export_paper_table.py real-world/ref-result
  tools/export_paper_table.py real-world/rumqtt/results/<stamp>-paper \\
                              real-world/pingora/results/<stamp>-paper \\
                              real-world/axum/results/<stamp>-paper
"""
import csv, glob, math, os, sys

def trim2(vals):
    s = sorted(vals)
    t = 2 if len(s) > 4 else (1 if len(s) > 2 else 0)
    kept = s[t:len(s)-t] if t else s
    m = sum(kept) / len(kept)
    sd = math.sqrt(sum((x-m)**2 for x in kept)/(len(kept)-1)) if len(kept) > 1 else 0.0
    return m, sd, len(vals)

# (label, system, workload, deployment, value_column_name)
ROWS = [
    ("rumqtt Fanout",        "rumqttd", "W-Fanout",  "cross", "pub_mps"),
    ("rumqtt Fanin",         "rumqttd", "W-Fanin",   "cross", "pub_mps"),
    ("rumqtt P2P",           "rumqttd", "W-P2P",     "cross", "pub_mps"),
    ("Pingora Low-conc",     "pingora", "conns50",   "cross", "rps"),
    ("Pingora High-conc",    "pingora", "conns200",  "cross", "rps"),
    ("Axum (cross) API",     "axum",    "small",     "cross", "rps"),
    ("Axum (cross) Static",  "axum",    "large",     "cross", "rps"),
    ("Axum (cross) Mixed",   "axum",    "mixed",     "cross", "rps"),
    ("Axum (local) API",     "axum",    "small",     "local", "rps"),
    ("Axum (local) Static",  "axum",    "large",     "local", "rps"),
    ("Axum (local) Mixed",   "axum",    "mixed",     "local", "rps"),
]

def load(dirs):
    """-> {(system, workload, deployment, runtime): [values]}"""
    data = {}
    for d in dirs:
        for path in glob.glob(os.path.join(d, "**", "*_raw.csv"), recursive=True):
            deployment = "local" if os.path.basename(path).endswith("_local_raw.csv") else "cross"
            with open(path) as f:
                for r in csv.DictReader(f):
                    system, runtime, workload = r.get("system"), r.get("runtime"), r.get("workload")
                    if not (system and runtime and workload):
                        continue
                    val = None
                    for col in ("pub_mps", "rps", "ops_per_sec"):
                        if r.get(col) not in (None, ""):
                            val = float(r[col]); break
                    if val is None:
                        continue
                    data.setdefault((system, workload, deployment, runtime), []).append(val)
    return data

def fmt_k(mean, sd):
    # adaptive units: sub-10K rates (e.g. the rate-limited Fanout) stay in
    # plain ops/s; everything else in K
    if mean < 10_000:
        return f"{mean:.0f}±{sd:.0f}/s"
    return f"{mean/1e3:.1f}±{sd/1e3:.1f}K"

def main():
    args = [a for a in sys.argv[1:] if not a.startswith("--")]
    fmt = "md"
    for a in sys.argv[1:]:
        if a.startswith("--format"):
            fmt = a.split("=", 1)[1] if "=" in a else "md"
    if not args:
        print(__doc__); sys.exit(1)
    data = load(args)
    if not data:
        print(f"no *_raw.csv found under: {' '.join(args)}", file=sys.stderr); sys.exit(2)

    table = []
    for label, system, wl, dep, _col in ROWS:
        t = data.get((system, wl, dep, "tokio"))
        l = data.get((system, wl, dep, "lion"))
        if not t or not l:
            table.append((label, None, None, None, None))
            continue
        tm, tsd, tn = trim2(t)
        lm, lsd, ln = trim2(l)
        ratio = 100.0 * lm / tm if tm else 0.0
        table.append((label, (tm, tsd), (lm, lsd), ratio, (tn, ln)))

    if fmt == "latex":
        print("% generated by tools/export_paper_table.py — trim-2 mean±std, Kops/s")
        print(r"\begin{tabular}{@{}l r r r@{}}")
        print(r"\toprule")
        print(r"Workload & Tokio & Lion & Lion/Tokio \\")
        print(r"\midrule")
        for label, t, l, ratio, _n in table:
            if t is None:
                print(f"{label} & \\multicolumn{{3}}{{c}}{{(no data)}} \\\\")
            else:
                print(f"{label} & {t[0]/1e3:.1f}$\\pm${t[1]/1e3:.1f} & "
                      f"{l[0]/1e3:.1f}$\\pm${l[1]/1e3:.1f} & {ratio:.1f}\\% \\\\")
        print(r"\bottomrule")
        print(r"\end{tabular}")
    elif fmt == "csv":
        print("workload,tokio_mean,tokio_std,lion_mean,lion_std,ratio_pct,runs_tokio,runs_lion")
        for label, t, l, ratio, n in table:
            if t is None:
                print(f"{label},,,,,,,"); continue
            print(f"{label},{t[0]:.2f},{t[1]:.2f},{l[0]:.2f},{l[1]:.2f},{ratio:.2f},{n[0]},{n[1]}")
    else:
        w = max(len(r[0]) for r in table)
        print(f"| {'Workload':<{w}} | {'Tokio':>12} | {'Lion':>12} | {'Lion/Tokio':>10} |")
        print(f"|{'-'*(w+2)}|{'-'*14}|{'-'*14}|{'-'*12}|")
        for label, t, l, ratio, _n in table:
            if t is None:
                print(f"| {label:<{w}} | {'—':>12} | {'—':>12} | {'—':>10} |")
            else:
                print(f"| {label:<{w}} | {fmt_k(*t):>12} | {fmt_k(*l):>12} | {ratio:>9.1f}% |")
        ratios = [r[3] for r in table if r[3]]
        if ratios:
            print(f"\nenvelope: {min(ratios):.1f}% – {max(ratios):.1f}% of Tokio "
                  f"({len(ratios)}/{len(table)} rows with data)")

if __name__ == "__main__":
    main()
