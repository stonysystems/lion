#!/usr/bin/env python3
"""Export the paper's IronFleet table (tab:ironfleet) from collected results.

Recomputes everything from the per-run raw files written by run.sh:
  {lion,csharp}_{unpin,1core}[.rN].reqlog   raw client stdout (#req lines)
  {lion,csharp}_{unpin,1core}[.rN].cpulog   per-second `ps -o %cpu` samples

Statistic: trim-2 mean +- sample std across reps (drop the two extreme runs
when n > 4, one of each when n > 2, none otherwise) — the project-wide rule.

Usage:
  ./export_table.py <results-dir> [--duration 30] [--format md|latex]
"""
import glob, math, os, re, sys

def trim2(vals):
    """Returns (mean, std, n_kept). With 3 reps this keeps exactly the median;
    n_kept==1 signals the +-std is undefined (display the bare value)."""
    s = sorted(vals)
    t = 2 if len(s) > 4 else (1 if len(s) > 2 else 0)
    kept = s[t:len(s)-t] if t else s
    m = sum(kept) / len(kept)
    sd = math.sqrt(sum((x-m)**2 for x in kept)/(len(kept)-1)) if len(kept) > 1 else 0.0
    return m, sd, len(kept)

def parse_rep(reqlog, duration):
    reqs = []
    for l in open(reqlog, errors="ignore"):
        m = re.match(r'#req\s+(\d+)\s+(\d+)\s+([\d.]+)', l)
        if m:
            reqs.append((int(m.group(1)), int(m.group(2)), float(m.group(3))))
    if not reqs:
        return None
    per = {}
    for tid, seq, _ in reqs:
        per[tid] = max(per.get(tid, 0), seq)
    lats = [l for _, _, l in reqs]
    # Peak over post-warmup samples only: `ps -o %cpu` is a lifetime average,
    # and the .NET startup burst inflates the first seconds (it decays toward
    # the steady state); skip the first 5 one-second samples.
    peak = 0.0
    cpulog = reqlog[:-len(".reqlog")] + ".cpulog"
    if os.path.exists(cpulog):
        for i, line in enumerate(open(cpulog)):
            if i < 5:
                continue
            for v in line.split():
                try:
                    peak = max(peak, float(v))
                except ValueError:
                    pass
    return sum(per.values()) / duration, sum(lats) / len(lats), peak

def main():
    args = [a for a in sys.argv[1:] if not a.startswith("--")]
    if not args:
        print(__doc__); sys.exit(1)
    duration, fmt = 30.0, "md"
    for a in sys.argv[1:]:
        if a.startswith("--duration"):
            duration = float(a.split("=", 1)[1])
        elif a.startswith("--format"):
            fmt = a.split("=", 1)[1]

    # cells[(runtime, config)] = ([thr...], [lat...], [cpu...]) across reps
    cells = {}
    for path in sorted(glob.glob(os.path.join(args[0], "**", "*.reqlog"), recursive=True)):
        m = re.match(r'(lion|csharp)_(unpin|1core)(\.r\d+)?\.reqlog$', os.path.basename(path))
        if not m:
            continue
        rep = parse_rep(path, duration)
        if rep is None:
            print(f"warning: no #req lines in {path}, skipped", file=sys.stderr)
            continue
        c = cells.setdefault((m.group(1), m.group(2)), ([], [], []))
        for lst, v in zip(c, rep):
            lst.append(v)
    if not cells:
        print(f"no parsable *.reqlog under {args[0]}", file=sys.stderr); sys.exit(2)

    cols = [("lion", "unpin"), ("lion", "1core"), ("csharp", "unpin"), ("csharp", "1core")]
    rows = [("Throughput (req/s)", 0, "{:.0f}±{:.0f}"),
            ("Avg latency (ms)",   1, "{:.2f}±{:.2f}"),
            ("Peak server CPU (%)", 2, "{:.0f}±{:.0f}")]

    def cell(col, idx, f):
        if col not in cells or not cells[col][idx]:
            return "—"
        m, sd, kept = trim2(cells[col][idx])
        if kept == 1:  # trim-2 of 3 reps = the median; +-std is undefined
            return f.split("±")[0].format(m)
        return f.format(m, sd)

    if fmt == "latex":
        print("% generated by export_table.py — trim-2 mean±std over reps")
        print(r"\begin{tabular}{@{}lrrrr@{}}")
        print(r"\toprule")
        print(r" & \multicolumn{2}{c}{\textbf{Lion}} & \multicolumn{2}{c}{\textbf{C\# IO}} \\")
        print(r"\textbf{Metric} & \textbf{unpin} & \textbf{1-core} & \textbf{unpin} & \textbf{1-core} \\")
        print(r"\midrule")
        for label, idx, f in rows:
            vals = " & ".join(cell(c, idx, f).replace("±", r"$\pm$") for c in cols)
            print(f"{label} & {vals} \\\\")
        print(r"\bottomrule")
        print(r"\end{tabular}")
    else:
        hdr = ["Metric", "Lion unpin", "Lion 1core", "C# unpin", "C# 1core"]
        print("| " + " | ".join(hdr) + " |")
        print("|" + "|".join("---" for _ in hdr) + "|")
        for label, idx, f in rows:
            print("| " + " | ".join([label] + [cell(c, idx, f) for c in cols]) + " |")
        for cfg in ("unpin", "1core"):
            l, c = cells.get(("lion", cfg)), cells.get(("csharp", cfg))
            if l and c and l[0] and c[0]:
                print(f"\nLion/C# throughput ({cfg}): {trim2(l[0])[0]/trim2(c[0])[0]:.2f}x")
        n = {k: len(v[0]) for k, v in cells.items()}
        print(f"reps per cell: {n}")

if __name__ == "__main__":
    main()
